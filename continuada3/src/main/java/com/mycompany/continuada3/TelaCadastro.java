    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Conexao conexao = new Conexao();
        JdbcTemplate vConexao = new JdbcTemplate(conexao.getvConexao());
        
        String nome = jNome.getText();
        String email = jEmail.getText();
        String senha = jSenha.getText();
        String senhaC = jSenhaC.getText();
        
        if(senha.equals(senhaC))
        {
            jTexto.setText("Cadastro realizado");
            jTexto.setForeground(Color.BLUE);
            if(jGerente.isSelected())
            {
                vConexao.update("insert into Funcionario (nome,email,senha,tipoF) values(?,?,?,?)", nome,email,senha,2);
            }
            else
            {
                vConexao.update("insert into Funcionario (nome,email,senha,tipoF) values(?,?,?,?)", nome,email,senha,1);
            }
            
        }
            else
        {
            jTexto.setText("Senhas n√£o coincidem");
            jTexto.setForeground(Color.red);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

