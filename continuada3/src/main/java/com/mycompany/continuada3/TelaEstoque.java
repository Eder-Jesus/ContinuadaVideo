    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Integer quan = Integer.valueOf(jQtnE.getText());
        Integer quanTotal = Integer.valueOf(jQuantiE.getText());
        Integer quanFinal = quanTotal + quan;
       
        vConexao.update("update produtos set quantidade= ? where nome = ?", quanFinal,jEstoque.getSelectedItem());
        jQtnE.setText(String.valueOf(quanFinal));
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jEstoqueItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jEstoqueItemStateChanged

        List<Produto> lista = vConexao.query("select descricao,quantidade from produtos where nome = ?", new BeanPropertyRowMapper(Produto.class), jEstoque.getSelectedItem());
        for(Produto prod: lista)
        {
         jDescE.setText(prod.getDescricao());
         jQtnE.setText(String.valueOf(prod.getQuantidade()));
            
            }

    }//GEN-LAST:event_jEstoqueItemStateChanged

